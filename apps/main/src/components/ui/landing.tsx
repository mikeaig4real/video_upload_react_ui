interface FlipCodeSkeletonProps {
  embedCode: string;
  iframeSrc: string;
}

export const FlipCodeSkeleton: React.FC<FlipCodeSkeletonProps> = ({
  embedCode,
  iframeSrc,
}) => {
  const [showCode, setShowCode] = useState(true);

  return (
    <div className="relative w-full max-w-xl mx-auto">
      <div className="flex gap-2 p-2 bg-transparent rounded-t-md">
        <button
          onClick={() => setShowCode(true)}
          className={`px-3 py-1 rounded-md text-sm font-medium ${
            showCode
              ? "bg-gray-300 dark:bg-gray-700"
              : "hover:bg-gray-200 dark:hover:bg-gray-800"
          }`}
        >
          Code
        </button>
        <button
          onClick={() => setShowCode(false)}
          className={`px-3 py-1 rounded-md text-sm font-medium ${
            !showCode
              ? "bg-gray-300 dark:bg-gray-700"
              : "hover:bg-gray-200 dark:hover:bg-gray-800"
          }`}
        >
          Preview
        </button>
      </div>

      <div className="relative h-[400px] w-full perspective">
        <AnimatePresence mode="wait" initial={false}>
          {showCode ? (
            <motion.div
              key="code"
              initial={{ rotateY: -90, opacity: 0 }}
              animate={{ rotateY: 0, opacity: 1 }}
              exit={{ rotateY: 90, opacity: 0 }}
              transition={{ duration: 0.5 }}
              className="absolute inset-0 backface-hidden overflow-auto text-left"
            >
              <CodeBlockDemo
                files={[
                  {
                    language: "html",
                    filename: "index.html",
                    code: embedCode,
                  },
                ]}
              />
            </motion.div>
          ) : (
            <motion.div
              key="preview"
              initial={{ rotateY: 90, opacity: 0 }}
              animate={{ rotateY: 0, opacity: 1 }}
              exit={{ rotateY: -90, opacity: 0 }}
              transition={{ duration: 0.5 }}
              className="absolute inset-0 backface-hidden flex items-center justify-center bg-black"
            >
              <iframe
                src={iframeSrc}
                width="100%"
                height="100%"
                className="rounded-md"
                frameBorder="0"
                allowFullScreen
              />
            </motion.div>
          )}
        </AnimatePresence>
      </div>
    </div>
  );
};

